def solve_quadratic(a, b, c):
    """
    –†–æ–∑–≤'—è–∑—É—î –∫–≤–∞–¥—Ä–∞—Ç–Ω–µ —Ä—ñ–≤–Ω—è–Ω–Ω—è ax¬≤ + bx + c = 0
    –ü–æ–≤–µ—Ä—Ç–∞—î –∫–æ—Ä—Ç–µ–∂ –∑ –∫–æ—Ä–µ–Ω—è–º–∏ (x1, x2) –∞–±–æ None —è–∫—â–æ –Ω–µ–º–∞—î –∫–æ—Ä–µ–Ω—ñ–≤
    """
    discriminant = b**2 - 4*a*c

    if discriminant < 0:
        return None  # –ù–µ–º–∞—î –¥—ñ–π—Å–Ω–∏—Ö –∫–æ—Ä–µ–Ω—ñ–≤
    elif discriminant == 0:
        x = -b / (2*a)
        return (x, x)
    else:
        sqrt_d = math.sqrt(discriminant)
        x1 = (-b + sqrt_d) / (2*a)
        x2 = (-b - sqrt_d) / (2*a)
        return (x1, x2)

# –¢–µ—Å—Ç—É–≤–∞–Ω–Ω—è
equations = [
    (1, -5, 6),      # x¬≤ - 5x + 6 = 0, –∫–æ—Ä–µ–Ω—ñ: 2, 3
    (1, 0, -4),      # x¬≤ - 4 = 0, –∫–æ—Ä–µ–Ω—ñ: 2, -2
    (1, -2, 1),      # x¬≤ - 2x + 1 = 0, –∫–æ—Ä—ñ–Ω—å: 1
    (1, 0, 1),       # x¬≤ + 1 = 0, –Ω–µ–º–∞—î –∫–æ—Ä–µ–Ω—ñ–≤
]

for a, b, c in equations:
    roots = solve_quadratic(a, b, c)
    if roots:
        print(f"  {a}x¬≤ + {b}x + {c} = 0")
        if roots[0] == roots[1]:
            print(f"    –û–¥–∏–Ω –∫–æ—Ä—ñ–Ω—å: x = {roots[0]}")
        else:
            print(f"    –î–≤–∞ –∫–æ—Ä–µ–Ω—ñ: x‚ÇÅ = {roots[0]:.2f}, x‚ÇÇ = {roots[1]:.2f}")
    else:
        print(f"  {a}x¬≤ + {b}x + {c} = 0")
        print(f"    –ù–µ–º–∞—î –¥—ñ–π—Å–Ω–∏—Ö –∫–æ—Ä–µ–Ω—ñ–≤")
print()

