def count_lines(filename):
    """–õ—ñ—á–∏—Ç—å –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Ä—è–¥–∫—ñ–≤ —É —Ñ–∞–π–ª—ñ."""
