def format_phone(phone):
    """–§–æ—Ä–º–∞—Ç—É—î –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω—É."""
    # –í–∏–¥–∞–ª—è—î–º–æ –≤—Å—ñ non-—Ü–∏—Ñ—Ä–æ–≤—ñ —Å–∏–º–≤–æ–ª–∏
    digits = re.sub(r'\D', '', phone)

    # –§–æ—Ä–º–∞—Ç—É—î–º–æ —è–∫ +38(XX)XXX-XXXX
    if len(digits) == 10:
        return f"+38({digits[:2]}){digits[2:5]}-{digits[5:]}"
    else:
        return "–ù–µ–≤—ñ—Ä–Ω–∏–π —Ñ–æ—Ä–º–∞—Ç"

