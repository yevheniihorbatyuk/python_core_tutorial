import math

def area_circle(r):
    """–ü–ª–æ—â–∞ –∫–æ–ª–∞"""
    return math.pi * r ** 2

def area_triangle(a, b, c):
    """–ü–ª–æ—â–∞ —Ç—Ä–∏–∫—É—Ç–Ω–∏–∫–∞ –∑–∞ —Ñ–æ—Ä–º—É–ª–æ—é –ì–µ—Ä–æ–Ω–∞"""
    s = (a + b + c) / 2
    return math.sqrt(s * (s - a) * (s - b) * (s - c))

def area_rectangle(a, b):
    """–ü–ª–æ—â–∞ –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫–∞"""
    return a * b

def distance(x1, y1, x2, y2):
    """–í—ñ–¥—Å—Ç–∞–Ω—å –º—ñ–∂ –¥–≤–æ–º–∞ —Ç–æ—á–∫–∞–º–∏"""
    return math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)

def is_prime(n):
    """–ü–µ—Ä–µ–≤—ñ—Ä—è—î —á–∏ —á–∏—Å–ª–æ –ø—Ä–æ—Å—Ç–µ"""
    if n < 2:
        return False
    for i in range(2, int(math.sqrt(n)) + 1):
        if n % i == 0:
            return False
    return True

def fibonacci(n):
    """n-—Ç–∏–π —á–ª–µ–Ω –ø–æ—Å–ª—ñ–¥–æ–≤–Ω–æ—Å—Ç—ñ –§—ñ–±–æ–Ω–∞—á—á—ñ"""
    if n <= 0:
        return 0
    elif n == 1:
        return 1
    else:
        a, b = 0, 1
        for _ in range(2, n):
            a, b = b, a + b
        return b

