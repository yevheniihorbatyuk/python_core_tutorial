def parse_logs(filename):
    """–ü–∞—Ä—Å–∏—Ç—å –ª–æ–≥–∏"""
    import re

    logs = []
    try:
        with open(filename, 'r', encoding='utf-8') as file:
            for line in file:
                match = re.search(r'(\d{4}-\d{2}-\d{2}) (\d{2}:\d{2}:\d{2}) \[(\w+)\] (.+)', line)
                if match:
                    logs.append({
                        'date': match.group(1),
                        'time': match.group(2),
                        'level': match.group(3),
                        'message': match.group(4)
                    })
    except FileNotFoundError:
        print(f"–§–∞–π–ª {filename} –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ")

    return logs

def filter_logs_by_level(logs, level):
    """–§—ñ–ª—å—Ç—Ä—É—î –ª–æ–≥–∏ –ø–æ —Ä—ñ–≤–Ω—é"""
    return [log for log in logs if log['level'] == level]

