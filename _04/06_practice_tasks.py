import csv
import json

class CSVtoJSON:
    """–ö–ª–∞—Å –¥–ª—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü—ñ—ó CSV —É JSON"""

    def __init__(self, csv_filename):
        self.csv_filename = csv_filename
        self.data = []
        self.load()

    def load(self):
        """–ó–∞–≤–∞–Ω—Ç–∞–∂—É—î CSV —Ñ–∞–π–ª"""
        try:
            with open(self.csv_filename, 'r', encoding='utf-8') as file:
                reader = csv.DictReader(file)
                self.data = list(reader)
        except FileNotFoundError:
            print(f"–§–∞–π–ª {self.csv_filename} –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ")

    def filter_by_age(self, min_age=None, max_age=None):
        """–§—ñ–ª—å—Ç—Ä—É—î –ø–æ –≤—ñ–∫—É"""
        if 'age' not in self.data[0]:
            return self

        filtered = []
        for row in self.data:
            try:
                age = int(row['age'])
                if min_age and age < min_age:
                    continue
                if max_age and age > max_age:
                    continue
                filtered.append(row)
            except ValueError:
                pass

        self.data = filtered
        return self

    def to_json(self, json_filename):
        """–ó–±–µ—Ä—ñ–≥–∞—î —É JSON"""
        try:
            with open(json_filename, 'w', encoding='utf-8') as file:
                json.dump(self.data, file, ensure_ascii=False, indent=2)
            print(f"–ó–±–µ—Ä–µ–∂–µ–Ω–æ —É {json_filename}")
        except Exception as e:
            print(f"–ü–æ–º–∏–ª–∫–∞: {e}")

