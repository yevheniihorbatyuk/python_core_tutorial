def file_stats(filename):
    """–û–±—á–∏—Å–ª—é—î —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É —Ñ–∞–π–ª—É"""
    try:
        with open(filename, "r", encoding="utf-8") as file:
            content = file.read()

        lines = content.split("\n")
        words = content.split()
        chars = len(content)

        return {
            "lines": len([l for l in lines if l.strip()]),  # –ù–µ–ø—É—Å—Ç—ñ —Ä—è–¥–∫–∏
            "words": len(words),
            "chars": chars
        }
    except FileNotFoundError:
        return None

