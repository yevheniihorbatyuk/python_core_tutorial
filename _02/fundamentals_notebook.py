def divide_with_remainder(a, b):
    """
    –î—ñ–ª–∏—Ç—å —á–∏—Å–ª–∞ –∑ –æ—Å—Ç–∞—á–µ—é
    
    Args:
        a: –î—ñ–ª–µ–Ω–µ
        b: –î—ñ–ª—å–Ω–∏–∫
    
    Returns:
        tuple: (—á–∞—Å—Ç–∫–∞, –æ—Å—Ç–∞—á–∞)
    """
    quotient = a // b
    remainder = a % b
    return quotient, remainder

# –†–æ–∑–ø–∞–∫—É–≤–∞–Ω–Ω—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç—É
