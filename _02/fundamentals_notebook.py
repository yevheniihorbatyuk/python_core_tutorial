def factorial(n):
    """
    –û–±—á–∏—Å–ª—é—î —Ñ–∞–∫—Ç–æ—Ä—ñ–∞–ª —á–∏—Å–ª–∞ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ
    
    Args:
        n (int): –ù–µ–≤—ñ–¥'—î–º–Ω–µ —Ü—ñ–ª–µ —á–∏—Å–ª–æ
    
    Returns:
        int: n! = n * (n-1) * (n-2) * ... * 1
    """
    if n == 0 or n == 1:
        return 1
    return n * factorial(n - 1)

