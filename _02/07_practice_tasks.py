def is_palindrome(text):
    """
    –ü–µ—Ä–µ–≤—ñ—Ä—è—î —á–∏ —î —Ä—è–¥–æ–∫ –ø–∞–ª—ñ–Ω–¥—Ä–æ–º–æ–º
    (—á–∏—Ç–∞—î—Ç—å—Å—è –æ–¥–Ω–∞–∫–æ–≤–æ –∑–ª—ñ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ —ñ —Å–ø—Ä–∞–≤–∞ –Ω–∞–ª—ñ–≤–æ)
    
    Args:
        text (str): –†—è–¥–æ–∫ –¥–ª—è –ø–µ—Ä–µ–≤—ñ—Ä–∫–∏
    
    Returns:
        bool: True —è–∫—â–æ –ø–∞–ª—ñ–Ω–¥—Ä–æ–º
    
    –ü—Ä–∏–∫–ª–∞–¥–∏:
        >>> is_palindrome("—à–∞–ª–∞—à")
        True
        >>> is_palindrome("python")
        False
    """
    # TODO: –ü–æ—Ä—ñ–≤–Ω—è–π—Ç–µ text –∑ text[::-1]
    # –ù–µ –∑–∞–±—É–¥—å—Ç–µ –ø—Ä–∏–≤–µ—Å—Ç–∏ –¥–æ –Ω–∏–∂–Ω—å–æ–≥–æ —Ä–µ–≥—ñ—Å—Ç—Ä—É —Ç–∞ –≤–∏–¥–∞–ª–∏—Ç–∏ –ø—Ä–æ–±—ñ–ª–∏
    
    # –û—á–∏—â–µ–Ω–Ω—è: –≤–∏–¥–∞–ª—è—î–º–æ –ø—Ä–æ–±—ñ–ª–∏ —Ç–∞ –ø—Ä–∏–≤–æ–¥–∏–º–æ –¥–æ –Ω–∏–∂–Ω—å–æ–≥–æ —Ä–µ–≥—ñ—Å—Ç—Ä—É
    cleaned = text.replace(" ", "").lower()
    
    # –ü–æ—Ä—ñ–≤–Ω—é—î–º–æ –∑ —Ä–µ–≤–µ—Ä—Å–æ–º
    return cleaned == cleaned[::-1]


