def temperature_converter(temp, from_scale, to_scale):
    """
    –ö–æ–Ω–≤–µ—Ä—Ç—É—î —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—É –º—ñ–∂ —à–∫–∞–ª–∞–º–∏ –¶–µ–ª—å—Å—ñ—è, –§–∞—Ä–µ–Ω–≥–µ–π—Ç–∞ —Ç–∞ –ö–µ–ª—å–≤—ñ–Ω–∞
    
    Args:
        temp (float): –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞
        from_scale (str): –í–∏—Ö—ñ–¥–Ω–∞ —à–∫–∞–ª–∞ ('C', 'F', 'K')
        to_scale (str): –¶—ñ–ª—å–æ–≤–∞ —à–∫–∞–ª–∞ ('C', 'F', 'K')
    
    Returns:
        float: –ö–æ–Ω–≤–µ—Ä—Ç–æ–≤–∞–Ω–∞ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞
    
    –§–æ—Ä–º—É–ª–∏:
        C ‚Üí F: (C √ó 9/5) + 32
        F ‚Üí C: (F - 32) √ó 5/9
        C ‚Üí K: C + 273.15
        K ‚Üí C: K - 273.15
    """
    # TODO: –†–µ–∞–ª—ñ–∑—É–π—Ç–µ –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü—ñ—é
    # –°–ø–æ—á–∞—Ç–∫—É –∫–æ–Ω–≤–µ—Ä—Ç—É–π—Ç–µ –≤—Å–µ –≤ –¶–µ–ª—å—Å—ñ–π, –ø–æ—Ç—ñ–º –≤ –ø–æ—Ç—Ä—ñ–±–Ω—É —à–∫–∞–ª—É
    
    # –ö–æ–Ω–≤–µ—Ä—Ç—É—î–º–æ –≤ –¶–µ–ª—å—Å—ñ–π
    if from_scale == 'C':
        celsius = temp
    elif from_scale == 'F':
        celsius = (temp - 32) * 5/9
    elif from_scale == 'K':
        celsius = temp - 273.15
    else:
        return None
    
    # –ö–æ–Ω–≤–µ—Ä—Ç—É—î–º–æ –∑ –¶–µ–ª—å—Å—ñ—è –≤ –ø–æ—Ç—Ä—ñ–±–Ω—É —à–∫–∞–ª—É
    if to_scale == 'C':
        return celsius
    elif to_scale == 'F':
        return (celsius * 9/5) + 32
    elif to_scale == 'K':
        return celsius + 273.15
    else:
        return None


print("\n--- –ó–∞–≤–¥–∞–Ω–Ω—è 12: –ö–æ–Ω–≤–µ—Ä—Ç–µ—Ä —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä ---")
