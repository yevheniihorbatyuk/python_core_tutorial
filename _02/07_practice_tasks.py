def find_letter_loop(text, letter):
    """
    –ó–Ω–∞—Ö–æ–¥–∏—Ç—å –≤—Å—ñ –ø–æ–∑–∏—Ü—ñ—ó –ª—ñ—Ç–µ—Ä–∏ —É —Ç–µ–∫—Å—Ç—ñ –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—á–∏ —Ü–∏–∫–ª for
    
    Args:
        text (str): –¢–µ–∫—Å—Ç –¥–ª—è –ø–æ—à—É–∫—É
        letter (str): –õ—ñ—Ç–µ—Ä–∞ –¥–ª—è –ø–æ—à—É–∫—É
    
    Returns:
        list: –°–ø–∏—Å–æ–∫ —ñ–Ω–¥–µ–∫—Å—ñ–≤ –¥–µ –∑–Ω–∞–π–¥–µ–Ω–æ –ª—ñ—Ç–µ—Ä—É
    
    –ü—Ä–∏–∫–ª–∞–¥:
        >>> find_letter_loop("Hello", "l")
        [2, 3]
    """
    positions = []
    
    # TODO: –ù–∞–ø–∏—à—ñ—Ç—å —Ü–∏–∫–ª for, —è–∫–∏–π –ø—Ä–æ—Ö–æ–¥–∏—Ç—å –ø–æ –∫–æ–∂–Ω–æ–º—É —Å–∏–º–≤–æ–ª—É
    # –í–∏–∫–æ—Ä–∏—Å—Ç–∞–π—Ç–µ enumerate() –∞–±–æ range(len(text))
    # –Ø–∫—â–æ —Å–∏–º–≤–æ–ª —Å–ø—ñ–≤–ø–∞–¥–∞—î –∑ –ª—ñ—Ç–µ—Ä–æ—é (–Ω–µ–∑–∞–ª–µ–∂–Ω–æ –≤—ñ–¥ —Ä–µ–≥—ñ—Å—Ç—Ä—É) - –¥–æ–¥–∞–π—Ç–µ —ñ–Ω–¥–µ–∫—Å –¥–æ positions
    
    for i in range(len(text)):
        if text[i].lower() == letter.lower():
            positions.append(i)
    
    return positions


