def rectangle_area_and_perimeter(width, height):
    """
    –û–±—á–∏—Å–ª—é—î –ø–ª–æ—â—É —Ç–∞ –ø–µ—Ä–∏–º–µ—Ç—Ä –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫–∞
    
    Args:
        width (float): –®–∏—Ä–∏–Ω–∞
        height (float): –í–∏—Å–æ—Ç–∞
    
    Returns:
        tuple: (–ø–ª–æ—â–∞, –ø–µ—Ä–∏–º–µ—Ç—Ä)
    """
